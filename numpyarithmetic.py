import numpy as np

array1 = np.array([1, 3, 5, 7])

print("First array is: ", array1)
print('\n')

array2 = np.array([2, 4, 6, 8])

print("Second array is: ", array2)
print('\n')

#Addition
print("Addition of both arrays: ")
print(np.add(array1, array2))
print('\n')

#Subtraction
print("Subtraction of both arrays: ")
print(np.subtract(array1, array2))
print('\n')

#Multiplication
print("Multiplication of both arrays: ")
print(np.multiply(array1, array2))
print('\n')

#Division
print("Division of both arrays: ")
print(np.divide(array1, array2))
print('\n')
